// Iron Man Helmet Toggle
function toggleHelmet() {
    document.getElementById("helmet").classList.toggle("open-mode");
}

// Lightsaber Toggle
function toggleLightsaber() {
    document.getElementById("lightsaber").classList.toggle("activated");
}

// Invisibility Cloak Toggle
function toggleCloak() {
    document.getElementById("cloak").classList.toggle("invisible");
}

// AI-HUD Display Toggle
function toggleHUD() {
    document.getElementById("aiHUD").classList.toggle("display-on");
}

// Cybernetic Arm Toggle
function toggleCyberArm() {
    document.getElementById("cyberArm").classList.toggle("power-mode");
}
